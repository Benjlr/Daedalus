            var retval = new List<double>();
            for (int i = 0; i < length / 2; i++)
            for (int x = 0; x < 8; x++)
                retval.Add(-0.5 + x * 0.12);

            for (int i = 0; i < length / 2; i++)
            for (int x = 0; x < 5; x++)
                retval.Add(-0.32 + x * 0.6);

            for (int i = 0; i < retval.Count; i++) {
                if (i % 4 == 0) retval[i] = 0;
